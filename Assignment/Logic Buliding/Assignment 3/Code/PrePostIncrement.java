class PrePostIncrement{
    public static void main(String[] args){
        int b = 3;
        // Pre Increment
        ++b;
        System.out.println("Pre Increment: "+ b);
        // Post Decrement
        b--;
        System.out.println("Post Decrement: "+b);

    }
}

// output 
// Pre Increment: 4
// Post Decrement: 3